    private void button_MegrendelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_MegrendelActionPerformed
        // TODO add your handling code here:
        String print_szoveg = "";
        


        String jelen_szoveg = label_KosarbanDarab.getText();
        int jelen_szam = parseInt(jelen_szoveg) ;
        
        
        String cim_szoveg = textfield_Cim.getText();
        String telszam_szoveg = textfield_Telszam.getText();
        
        String valasztott_pizza = (String) combobox_PizzaTipus.getSelectedItem();
        
        if (checkbox_Gluten.isSelected()) {
            valasztott_pizza += " Pizza (Gluténmentes)";
        } else {
            valasztott_pizza += " Pizza";
        }
        
        
        if (jelen_szam == 0) {
            print_szoveg = "0 db van a kosárban!";
        } else {
            if (cim_szoveg.length() < 7) {
                print_szoveg = "Kérlek létező címet adj meg!";
        }   else if (cim_szoveg.length() < 10) {
                print_szoveg = "Kérlek létező telefonszámot adj meg!";
        }   else
        {       print_szoveg = "Rendelés: " + valasztott_pizza + "\nVégösszeg: " + Vegosszeg + " Ft\nCím: " + cim_szoveg + "\nKiszállítási idő: 45 perc"; }
        }
        
        
        
        JOptionPane.showMessageDialog(rootPane, print_szoveg);
    }//GEN-LAST:event_button_MegrendelActionPerformed

