    private void button_KosarbaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_KosarbaActionPerformed
        // TODO add your handling code here:
        String valasztott_pizza = (String) combobox_PizzaTipus.getSelectedItem();
        
        
        String print_szoveg = "";
        
        if (valasztott_pizza == "- Válasszon -") {
            print_szoveg = "Kérem válasszon ki egy pizza fajtát!";
            JOptionPane.showMessageDialog(rootPane, print_szoveg);
        } else {
            // megy tovább a kód
        }

        
        String jelen_szoveg = label_KosarbanDarab.getText();
        int jelen_szam = parseInt(jelen_szoveg) ;
        jelen_szam += 1;
        
        if (jelen_szam > 3) {
            JOptionPane.showMessageDialog(rootPane, "A maximum 3 db!");
        } else {
            // frissiti a label_K. tartalmát
            label_KosarbanDarab.setText(Integer.toString(jelen_szam));
        }
        
        
        Vegosszeg = jelen_szam * 1900;
        label_VegosszegFt.setText(Vegosszeg+" Ft");

    }//GEN-LAST:event_button_KosarbaActionPerformed

